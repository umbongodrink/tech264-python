
# What is a variable?
# A variable is a named storage location in a computer's memory that holds a value.
# The value of a variable can be changed, hence the name variable.

## Points to remember about variables in Python:
# 1. should be descriptive and follow naming conventions
# 2. should not start with a number
# 3. should not contain spaces

# 4. can be of varying data types
# 5. can be assigned with = operator
# 6. can be reassigned


